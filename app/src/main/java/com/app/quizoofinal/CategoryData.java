package com.app.quizoofinal;

public class CategoryData extends CategoryAdapter {
     String name,image;
     public CategoryData(){}

    public CategoryData(String name, String image) {
        this.name = name;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
}

